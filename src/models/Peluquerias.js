class Peluquerias {
    constructor(idMunicipio, nombrePeluqueria, nombreContacto, dni, nCuenta, direccion, correo,
                telefono, observaciones, visitas, ultimaVisita, totalCobrado, diaCierre) {
        this.idMunicipio = idMunicipio;
        this.nombrePeluqueria = nombrePeluqueria;
        this.nombreContacto = nombreContacto;
        this.dni = dni;
        this.nCuenta = nCuenta;
        this.direccion = direccion;
        this.correo = correo;
        this.telefono = telefono;
        this.observaciones = observaciones;
        this.visitas = visitas;
        this.ultimaVisita = ultimaVisita;
        this.totalCobrado = totalCobrado;
        this.diaCierre = diaCierre;
    }
}

export default Peluquerias;